void loaddataComBoboxkh(){
    DefaultComboBoxModel model=(DefaultComboBoxModel) cbbkhoahoc.getModel();
    model.removeAllElements();
    try {
        List<KhoaHoc> list=khdao.selectAll();
        for(KhoaHoc kh:list){
           model.addElement(kh);
        }
    } catch (Exception e) {
        TBBOX.alert(this, "Lỗi truy vấn");
    }
}
String classifyStudent(double score) {
        if (score >= 8 && score <= 10) {
            return "Giỏi";
        } else if (score >= 6.5 && score < 8.0) {
            return "Khá";
        } else if (score >= 5 && score < 6.5) {
            return "Trung bình";
        } else if (score < 5) {
            return "Yếu";
        } else {
            return "Điểm không hợp lệ";
        }
    }
