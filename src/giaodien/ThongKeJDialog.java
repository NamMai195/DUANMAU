void loaddatat0tablehv(){
    DefaultTableModel model=(DefaultTableModel) tblhvkh.getModel();
    model.setRowCount(0);
    try {
       KhoaHoc khoahoc=(KhoaHoc) cbbkhoahoc.getSelectedItem();
       
           List<Object[]> list=tkdao.getBangDem(khoahoc.getMaKH());
           for(Object[] row:list){
               String diem=classifyStudent((double) row[2]);
               model.addRow(new Object[]{row[0],row[1],row[2],diem});

           }
      
    } catch (Exception e) {
        System.out.println(e);
    }
}
